/* ============================================================
   DÉTECTION AUTOMATIQUE DE LA LANGUE
   Ordre de priorité :
   1. Préférence sauvegardée (localStorage)
   2. navigator.language
   3. Français par défaut
   ============================================================ */
function _detectLang() {
  const saved = localStorage.getItem('ls_lang');
  if (saved && I18N_DATA[saved]) return saved;

  const nav = (navigator.language || navigator.userLanguage || 'fr').toLowerCase();

  // Correspondance exacte code-2
  const code2 = nav.split('-')[0];
  if (I18N_DATA[code2]) return code2;

  // Correspondances spéciales
  if (nav.startsWith('zh')) return 'zh';
  if (nav.startsWith('pt')) return 'pt';
  if (nav.startsWith('ar')) return 'ar';

  return 'fr';
}

/* ============================================================
   API PUBLIQUE
   ============================================================ */
const I18N = (() => {
  let _lang = _detectLang();

  /**
   * Retourne la chaîne traduite avec interpolation.
   * Supporte {0}, {1}, ... comme placeholders.
   * @param {string} key
   * @param {...string} args
   */
  function t(key, ...args) {
    const str = I18N_DATA[_lang]?.[key] ?? I18N_DATA.fr?.[key] ?? key;
    if (!args.length) return str;
    return str.replace(/\{(\d+)\}/g, (_, i) => args[i] ?? '');
  }

  /** Retourne un tableau traduit (months, days, etc.) */
  function arr(key) {
    return I18N_DATA[_lang]?.[key] ?? I18N_DATA.fr[key] ?? [];
  }

  /** Change la langue active */
  function setLang(lang) {
    if (!I18N_DATA[lang]) return;
    _lang = lang;
    localStorage.setItem('ls_lang', lang);
  }

  /** Langue courante */
  function getLang() { return _lang; }

  /** Toutes les langues disponibles */
  function getAvailable() { return Object.keys(I18N_DATA); }

  return { t, arr, setLang, getLang, getAvailable };
})();

/* Exposer globalement */
window.I18N = I18N;
window.t    = I18N.t.bind(I18N);
